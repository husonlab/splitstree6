    private boolean isInBounds(double point, double min, double max){
        return (point > min) && (point < max);
    }


    private boolean containsCountry(Country country, double minLat, double maxLat, double minLong, double maxLong){
        if(isInBounds(country.min_lat, minLat, maxLat) || isInBounds(country.max_lat, minLat, maxLat) || (country.min_lat < minLat && country.max_lat > maxLat)){
            if(isInBounds(country.min_lon, minLong, maxLong) || isInBounds(country.max_lon, minLong, maxLong) || (country.min_lon < minLong && country.max_lon > maxLong))return true;
            else return false;
        }else return false;
    }

    private Country moveCenter(Country country, double minLat, double maxLat, double minLong, double maxLong){
        double centerLat;
        double centerLong;

        // Place the center of the country inside the bounds that are displayed
        if(isInBounds(country.center_lat, minLat, maxLat))centerLat = country.center_lat;
        else if (country.min_lat > minLat)centerLat = (country.min_lat + maxLat) / 2;
        else if (country.max_lat < maxLat)centerLat = (minLat + country.max_lat) / 2;
        else centerLat = (minLat + maxLat) / 2;

        if(isInBounds(country.center_lon, minLong, maxLong))centerLong = country.center_lon;
        else if (country.min_lon > minLong)centerLong = (country.min_lon + maxLong) / 2;
        else if (country.max_lon < maxLong)centerLong = (minLong + country.max_lon) / 2;
        else centerLong = (minLong + maxLong) / 2;

        return new Country(country.name, country.min_lat, country.max_lat, country.min_lon, country.max_lon, centerLat, centerLong);
    }
